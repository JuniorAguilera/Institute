<?php require_once('../../globales_sistema.php'); ?>
function insert(){
var id = $('#id').val();

var nombre = $('#nombre').val();

$.post('ws/estado_estudiante.php', {op: 'add',id:id,nombre:nombre}, function(data) {
if(data === 0){
$('#merror').show('fast').delay(4000).hide('fast');
}
else{
$('#msuccess').show('fast').delay(4000).hide('fast');
location.reload();
}
}, 'json');
}
function update(){
